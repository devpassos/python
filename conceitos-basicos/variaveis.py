# A declaração de variáveis em python é muito simples.
# Diferente de outas linguagens, não é necessário informar o tipo da variável, pois o interpretador infere o seu tipo.
# Não é necessário informar nenhuma palavra reservada antes da variável. Basta colocar nome e atribuir um valor à mesma.

nome = "Giropops"

######################################################################
# Imprime no console o conteúdo da variável nome
print (nome)


# Python é uma linguagem faracamente tipada, ou seja, os tipos de dados de suas variáveis, não precisam ser declarados
# Também trabalha com o conceito de tipos dinâmicos, de modo que uma mesma variável pode receber diversos valores em momentos diferentes

variavelDinamica = "Eu sou uma string" # Declaração de uma string
print(variavelDinamica)


variavelDinamica = 99882 # Atribuindo um número
print(variavelDinamica)


